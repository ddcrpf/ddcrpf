-- Create a view of Test1 marks of student USN ‘1BI15CS101’ in allsubjects.

CREATE VIEW
STU_TEST1_MARKS_VIEW AS SELECT
TEST1, SUBCODE
FROM IAMARKS
WHERE USN = '1RN13CS091';

-- Calculate the FinalIA (average of best two test marks) and update the corresponding table for allstudents.
UPDATE IAMARKS I
SET FinalIA = (
    SELECT (Test1 + Test2 + Test3 + FinalIA - LEAST(Test1, Test2, Test3, FinalIA)) / 3
    FROM (
        SELECT Test1, Test2, Test3, FinalIA
        FROM IAMARKS
        WHERE USN = I.USN AND Subcode = I.Subcode AND SSID = I.SSID
        ORDER BY Test1 DESC, Test2 DESC, Test3 DESC, FinalIA DESC
        LIMIT 2
    ) AS BestTwo
)
WHERE EXISTS (
    SELECT 1
    FROM IAMARKS
    WHERE USN = I.USN AND Subcode = I.Subcode AND SSID = I.SSID
);






-- Categorize students based on the followingcriterion: If FinalIA = 17 to 20 then CAT=‘Outstanding’
-- If FinalIA = 12 to 16 then CAT = ‘Average’ If FinalIA< 12 then CAT = ‘Weak’ Give these details only for
-- 8th semester A, B, and C section students.

SELECT S.USN,S.SNAME,S.ADDRESS,S.PHONE,S.GENDER, (CASE
WHEN IA.FINALIA BETWEEN 17 AND 20 THEN'OUTSTANDING' WHEN IA.FINALIA BETWEEN 12 AND 16 THEN 'AVERAGE' ELSE 'WEAK'
END) AS CAT
FROM STUDENT S, SEMSEC SS, IAMARKS IA, SUBJECT SUB WHERE S.USN = IA.USN AND SS.SSID = IA.SSID AND
SUB.SUBCODE = IA.SUBCODE
AND SUB.SEM = 8;